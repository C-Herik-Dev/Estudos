// Operadores aritméticos

// Adição (+) -> e faz concatenação de strings
// Exemplo: console.log(5 + 5); // 10
// Exemplo: console.log("5" + "5"); // 55 (concatenação)

// Subtração (-)
// Multiplicação (*)
// Divisão (/)
// Módulo (%) - Resto da divisão
// Exemplo: console.log(5 % 2); // sobra 1 (resto da divisão de 5 por 2)

// Exponenciação (**) - Potenciação eleva o primeiro número à potência do segundo
// Exemplo: console.log(2 ** 3); // 8 (2 elevado a 3)

// Operadores de atribuição
// Atribuição (=) - Atribui um valor a uma variável
// Exemplo: let x = 5; // x recebe o valor 5


// Incremento (++) - Adiciona 1 ao valor da variável
// Exemplo: x++; // x agora é 6
// Exemplo: ++x; // x agora é 6 (incrementa antes de usar o valor)


// Decremento (--) - Subtrai 1 do valor da variável
// Exemplo: x--; // x agora é 5
// Exemplo: --x; // x agora é 5 (decrementa antes de usar o valor)

// Atribuição com adição (+=) - Adiciona um valor à variável e atribui o resultado
// Exemplo: x += 5; // x agora é 10 (5 + 5)
// Atribuição com subtração (-=) - Subtrai um valor da variável e atribui o resultado
// Exemplo: x -= 2; // x agora é 8 (10 - 2)
// isso pode ser feito com todos os operadores aritméticos
// Exemplo: x *= 2; // x agora é 16 (8 * 2)
// Exemplo: x /= 4; // x agora é 4 (16 / 4)
// Exemplo: x %= 3; // x agora é 1 (4 % 3)
//...
// exemplo: x = 2;
// Exemplo: x **= 10; // x agora é 1024 (2 elevado a 10)

